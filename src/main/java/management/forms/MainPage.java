    private void displayBooksOnPieChart(){
        DefaultPieDataset barDataset = new DefaultPieDataset();
        JFreeChart piechart;
        
        try(Connection connection = BDConnection.getConnection()){
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery("SELECT c.denumire_carte, COUNT(*) as count "
                    + "FROM carti_oferite cf INNER JOIN carti c ON cf.id_carte = c.id_carte group by c.id_carte");
 
            while(rs.next()){
                barDataset.setValue(rs.getString("denumire_carte"), new Double(rs.getDouble("count")));   
            }
        }catch (SQLException ex){
            ex.printStackTrace();
        }

        piechart = ChartFactory.createPieChart("Statistica despre cărtile emise", barDataset, false, true, false);
        piechart.getTitle().setPaint(new Color(255,255,255));
        piechart.getTitle().setFont(new Font("Comic Sans MS", Font.BOLD, 24));
        piechart.setBackgroundPaint(new Color(51,51,51));
        
        PiePlot piePlot =(PiePlot) piechart.getPlot();
      
        piePlot.setBackgroundPaint(Color.GRAY);
        
        ChartPanel barChartPanel = new ChartPanel(piechart);
        panelPie.removeAll();
        panelPie.add(barChartPanel, BorderLayout.CENTER);
        panelPie.validate();
        
    }

