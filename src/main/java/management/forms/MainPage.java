    public final void setInfoOfQuantity(){
        Date date = new Date((long)System.currentTimeMillis());
        String sqlBooks, sqlStudents, sqlDebtors, sqlIssuedBooks;
        
        sqlBooks = "SELECT COUNT(*) as num FROM carti";
        sqlStudents = "SELECT COUNT(*) as num FROM studenti";
        sqlIssuedBooks = "SELECT COUNT(*) as num FROM carti_oferite WHERE status = 'In asteptare'";
        sqlDebtors = "SELECT COUNT(*) as num FROM carti_oferite WHERE data_oferirii < ? AND status = 'In asteptare'";
        
        try(Connection connection = BDConnection.getConnection()){
            Statement st = connection.createStatement();
            
            setTextNumToLabel(st, nrOfBooksLabel, sqlBooks);
            setTextNumToLabel(st, nrOfStudentsLabel, sqlStudents);
            setTextNumToLabel(st, nrOfIssuedBooksLabel, sqlIssuedBooks);
            
            PreparedStatement prepst = connection.prepareStatement(sqlDebtors);
            prepst.setDate(1, date);
            setTextNumToLabel(prepst, nrOfDebtorsLabel);   
        }catch (SQLException ex){
            ex.printStackTrace();
        }
    }
    
    private void setTextNumToLabel(Statement st, javax.swing.JLabel label, String sql) throws SQLException{ 
        ResultSet rs = st.executeQuery(sql);
        if(rs.next()){
            label.setText(rs.getString("num"));
        }
    }
    
    private void setTextNumToLabel(PreparedStatement prepst, javax.swing.JLabel label) throws SQLException{ 
        ResultSet rs = prepst.executeQuery();
        if(rs.next()){
            label.setText(rs.getString("num"));
        }
    }
    
    
